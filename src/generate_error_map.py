def preprocess(key, value):
    key = remove_accents(tokenize_text(key.lower()))
    value = remove_accents(tokenize_text(value.lower()))
    if len(key) == 1 and len(value) == 1:
        return ' '.join(key), ' '.join(value)
    else:
        return None, None


def generate_error_map():
    add_by_user(error_map)
    dicio_list(error_map)
    new_map = {}
    for key, value in error_map.items():

        incorrect, correct = preprocess(key, value)
        if (incorrect is not None or
                correct is not None) and incorrect != correct:
            new_map[incorrect] = correct

    print("Number of errors: " + str(len(error_map)))
    print("Number of considered errors: " + str(len(new_map)))

    with open(cns.SRC + 'error_map.py', 'w') as f:
        f.write("error_map = ")
        f.write(json.dumps(sort_dict(new_map), indent=2) + "\n")
    return new_map


